function du = ej9(x, u)
    % y'' = 1/8*(32 + 2*x^3 - y*y'), x en [1, 3]
    % u(1) = y  --> du(1) = u(2);
    % u(2) = y' --> du(2) = (1/8)*(32 + 2*x.^3 - u(1).*u(2));
    % u(3) = z  --> du(3) = u(4)
    % u(4) = z' --> du(4) = (df/dy)*z + (df/dy')*z' = (-1/8)*(u(3)*u(2) + u(4)*u(1));
    du = zeros(4,1);
    du(1) = u(2);
    du(2) = (1/8)*(32 + 2*x.^3 - u(1).*u(2));
    du(3) = u(4);
    du(4) = (-1/8)*u(2)*u(3)-(1/8)*u(1)*u(4);
end